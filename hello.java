// This is the class declaration. Every Java program must have at least one class.
public class HelloWorld {

    // This is the main method. It's the entry point of any Java application.
    // 'public' means it's accessible from anywhere.
    // 'static' means it belongs to the class, not a specific object.
    // 'void' means it doesn't return anything.
    // 'String[] args' is used to receive command-line arguments.
    public static void main(String[] args) {

        // This line prints "Hello world!" to the console.
        // 'System.out' refers to the standard output stream.
        // 'println()' prints the text and moves to the next line.
        System.out.println("Hello world!");
    }
}
